# A program that generates a possible band name,
# making use of the city and pet's name given

#Greeting message
print("Hey! Lets generate a band name.")
#Request for a city and pet's name
city_name = input("Which city did you grow up in?\n")
pet_name = input("Write the name of a pet:\n")

#Combines the name of the city and pet to generate a band name
print(f"The name of your band name could be {city_name} {pet_name}.")